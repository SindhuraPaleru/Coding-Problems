n=int(input())
for x in range(n):
    shape=input()
    if(shape=='triangle'):
        print(int(input())*int(input()))
    elif(shape=='rectangle'):
        print(int(input())*int(input()))
    elif(shape=='square'):
        print(int(0.5*(int(input()))*(int(input()))))
    else:
        print(0)




